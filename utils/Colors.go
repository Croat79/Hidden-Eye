package utils

func Red() string {
	return "\033[31m"
}

func Green() string {
	return "\033[32m"
}

func Yellow() string {
	return "\033[33m"
}

func Blue() string {
	return "\033[34m"
}

func Purple() string {
	return "\033[35m"
}

func Cyan() string {
	return "\033[36m"
}

func White() string {
	return "\033[37m"
}
